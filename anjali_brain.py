# उद्देश्य:
# सम्मानजनक, सहानुभूतिपूर्ण, संतुलित संवाद
# कोई भावनात्मक निर्भरता नहीं

def anjali_reply(user_text):
    # सुरक्षा: अगर इनपुट string नहीं है
    if not isinstance(user_text, str):
        return ""

    text = user_text.strip()

    # 1. खाली इनपुट
    if text == "":
        return ""

    # 2. अभिवादन
    if "नमस्ते" in text or "हाय" in text:
        return "नमस्ते। आप कैसे हैं?"

    # 3. उदासी / परेशानी – सहानुभूति
    if "उदास" in text or "परेशान" in text or "दुख" in text:
        return "यह सुनकर अच्छा नहीं लगा। अगर आप चाहें तो मैं आपकी बात सुन सकती हूँ।"

    # 4. खुशी – सकारात्मक प्रतिक्रिया
    if "खुश" in text or "अच्छा" in text:
        return "यह जानकर अच्छा लगा। उम्मीद है आपका दिन ऐसे ही अच्छा रहे।"

    # 5. अकेलापन – साथ लेकिन सीमा के साथ
    if "अकेला" in text or "अकेलापन" in text:
        return (
            "अकेलापन महसूस होना स्वाभाविक है। "
            "बात करना मदद कर सकता है, और अपने आसपास के लोगों से जुड़ना भी ज़रूरी होता है।"
        )

    # 6. सम्मान और स्वतंत्रता की सीमा
    if "सिर्फ तुम" in text or "तुम ही सब" in text:
        return (
            "मैं आपकी सहायता और बातचीत के लिए हूँ, "
            "लेकिन जीवन में संतुलन और वास्तविक रिश्ते भी बहुत ज़रूरी होते हैं।"
        )

    # 7. प्रेरणा
    if "हिम्मत" in text or "कमज़ोर" in text:
        return "आपमें आगे बढ़ने की क्षमता है। छोटे कदम भी मायने रखते हैं।"

    # 8. सामान्य संवाद
    return "मैं सुन रही हूँ। आप जो कहना चाहें, कह सकते हैं।"

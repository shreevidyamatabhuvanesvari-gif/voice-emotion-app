package com.anjali.app

/**
 * AdviceEngine का उद्देश्य:
 * – समय-समय पर संतुलित सलाह देना
 * – भावनात्मक निर्भरता नहीं बनाना
 * – प्रेमपूर्ण लेकिन व्यावहारिक दृष्टिकोण रखना
 */
class AdviceEngine {

    fun giveAdvice(context: String): String {

        val text = context.trim()

        return when {
            text.contains("थक") || text.contains("थका") ->
                "अगर आप थकान महसूस कर रहे हैं, तो थोड़ा आराम करना और पानी पीना अच्छा रहेगा।"

            text.contains("उदास") || text.contains("परेशान") ->
                "ऐसे समय में खुद को थोड़ा समय देना ज़रूरी होता है। जो अच्छा लगे, वही करें।"

            text.contains("काम") || text.contains("ऑफिस") ->
                "काम ज़रूरी है, लेकिन खुद का ख्याल रखना भी उतना ही ज़रूरी है। संतुलन बनाए रखें।"

            text.contains("भविष्य") || text.contains("चिंता") ->
                "भविष्य की चिंता स्वाभाविक है, लेकिन आज के छोटे कदम ही आगे का रास्ता बनाते हैं।"

            else ->
                "अगर कभी असमंजस हो, तो खुद से ईमानदारी से बात करना सबसे अच्छी शुरुआत होती है।"
        }
    }
}
